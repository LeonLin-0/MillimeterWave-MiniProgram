  drawHeartRateCanvas(data1) {
    let data = this.data.heartData;
    const query = wx.createSelectorQuery();
    query.select('#heartCanvas')
      .fields({node: true, size: true})
      .exec(res => {
        const canvas = res[0].node;
        if (canvas.getContext) {
          // 初始化画布
          const ctx = canvas.getContext('2d');
          const w = res[0].width;
          const h = res[0].height;
          const dpr = wx.getWindowInfo().pixelRatio;
          canvas.width = w * dpr;
          canvas.height = h * dpr;
          ctx.scale(dpr, dpr);
          // 清除上一次的内容
          ctx.clearRect(0, 0, w, h);
          var offsetx = 19;
          var offsety = 25;
          // 图的大小
          var width = 300; // 30列对应30s
          var height = 100; // 10行
          ctx.fillStyle = "rgba(255,255,255,1)"; // 填充白底
          ctx.fillRect(offsetx, offsety, width, height);

          var d = 10;
          // 画竖线
          for (var i = 0; i <= width / d; i++) {
            if (i % 5 == 0) {
              ctx.strokeStyle = "rgba(0,0,0,.5)";
            } else {
              ctx.strokeStyle = "rgba(0,0,0,.2)"
            }
            ctx.lineWidth = 1;
            ctx.beginPath();
            ctx.moveTo(i * d + offsetx, offsety);
            ctx.lineTo(i * d + offsetx, height + offsety);
            ctx.stroke();
          }
          // 画横线
          for (var j = 0; j <= height / d; j++) {
            if (j % 5 == 0) {
              ctx.strokeStyle = "rgba(0,0,0,.5)";
            } else {
              ctx.strokeStyle = "rgba(0,0,0,.2)";
            }
            ctx.lineWidth = 1;
            ctx.beginPath();
            ctx.moveTo(offsetx, j * d + offsety);
            ctx.lineTo(width + offsetx, j * d + offsety);
            ctx.stroke();
          }
          // 画标注
          ctx.font = "10px Microsoft YaHei";
          ctx.fillStyle = "rgb(0,0,0)";
          // 底部文字
          ctx.fillText("1秒/小格", offsetx + width / 2 - 20, 140);
          // 左侧标注
          ctx.fillText("1.5mv", 18, 18);
          ctx.fillText("0", 8, height / 2 + offsety + 3);
          ctx.fillText("-1.5mv", 18, 140);
          // 画波形图
          ctx.lineWidth = 1;
          for (var i=0; i<data.length-1; i++) {
            // 表总长为30s
            // 横坐标：1小格1s10px,共20个点,一个点0.5px,50/0.5=100
            // 纵坐标：1.5/5=0.3,1小格为0.3mv,为10px,10/0.3=33.3
            // 所有坐标保留两位小数
            var x0 = (data[i][0]/100.0 + offsetx).toFixed(2);
            var x1 = (data[i+1][0]/100.0 + offsetx).toFixed(2);
            var y0 = 75-(33.3*(+data[i][1]).toFixed(2)).toFixed(2);
            var y1 = 75-(33.3*(+data[i+1][1]).toFixed(2)).toFixed(2);
            ctx.strokeStyle = "rgb(244, 93, 93)";
            ctx.beginPath();
            // 修改精度
            ctx.moveTo(x0, y0);
            ctx.lineTo(x1, y1);
            ctx.stroke();
          }
        }
      })
  },
